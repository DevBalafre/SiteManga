<?php

namespace App\Entity;

use App\Repository\ChapterRepository;
use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity(repositoryClass=ChapterRepository::class)
 */
class Chapter
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $title;

    /**
     * @ORM\Column(type="datetime")
     */
    private $date_uploads;

    /**
     * @ORM\ManyToOne(targetEntity=Manga::class, inversedBy="chapters")
     */
    private $manga;

    /**
     * @ORM\OneToMany(targetEntity=Image::class, mappedBy="chapter")
     */
    private $images;

    public function __construct()
    {
        $this->images = new ArrayCollection();
    }

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getTitle(): ?string
    {
        return $this->title;
    }

    public function setTitle(string $title): self
    {
        $this->title = $title;

        return $this;
    }

    public function getDateUploads(): ?\DateTimeInterface
    {
        return $this->date_uploads;
    }

    public function setDateUploads(\DateTimeInterface $date_uploads): self
    {
        $this->date_uploads = $date_uploads;

        return $this;
    }
    // $manga = $chapter->getManga()
    // $manga->setUpdatedAt(new \Datetime);
    // $em->persist($manga);
    public function getManga(): ?Manga
    {
        return $this->manga;
    }

    public function setManga(?Manga $manga): self
    {
        $this->manga = $manga;

        return $this;
    }

    /**
     * @return Collection<int, Image>
     */
    public function getImages(): Collection
    {
        return $this->images;
    }

    public function addImage(Image $image): self
    {
        if (!$this->images->contains($image)) {
            $this->images[] = $image;
            $image->setChapter($this);
        }

        return $this;
    }

    public function removeImage(Image $image): self
    {
        if ($this->images->removeElement($image)) {
            // set the owning side to null (unless already changed)
            if ($image->getChapter() === $this) {
                $image->setChapter(null);
            }
        }

        return $this;
    }
}
